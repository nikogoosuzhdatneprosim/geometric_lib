def area(a, h):
    '''
    Возвращаем площадь треугольника.
    Параметры: a(int): основание
               h(int): высота
    Возвращаем: a * h / 2: нахождение площади треугольника
    '''
    return a * h / 2
def perimeter(a, b, c):
    '''
    Возвращаем периметр треугольника.
    Параметры: a(int), b(int), c(int): стороны треугольника
    Возвращаем: a + b + c: нахождение периметра треугольника
    '''
    return a + b + c
