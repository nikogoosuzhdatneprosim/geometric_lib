def area(a):
    '''
    Возвращаем площадь квадрата.
    Параметры: a(int): длина и ширина квадрата.
    Возвращаем: a * a: нахождение.
    '''
    return a * a
def perimeter(a):
    '''
    Возвращаем периметр квадрата.
    Параметры: a(int): длина и ширина квадрата.
    Возвращаем: 4 * a: нахождение периметра квадрата.
    '''
    return 4 * a
