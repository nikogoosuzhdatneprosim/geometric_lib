def area(a, b):
    '''
    Возвращаем площадь прямоугольника.
    Параметры: a(int): длина прямоугольника.
               b(int): ширина прямоугольника.
    Возвращаем: a * b: нахождение площади прямоугольника
    '''
    return a * b
def perimeter(a, b):
    '''
    Возвращаем периметр прямоугольника.
    Параметры: a(int): длина прямоугольника.
               b(int): ширина прямоугольника.
    Возвращаем: 2 * (a + b): нахождение периметра прямоугольника.
    '''
    return 2 * (a + b)
