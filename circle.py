import math
def area(r):
    '''
    Возвращаем площадь круга.
    Параметры: r(int): радиус.
    Возвращаем: math.pi * r * r: нахождение площади круга.
    '''
    return math.pi * r * r
def perimeter(r):
    '''
    Возвращаем периметр круга.
    Параметры: r(int): радиус.
    Возвращаем: 2 * math.pi * r: нахождение периметра круга.
    '''
    return 2 * math.pi * r
